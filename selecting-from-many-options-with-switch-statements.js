function caseInSwitch(val) {
    let answer = "";
    switch (val) {
      case 1:
      answer = answer + "alpha";
      break;
      case 2:
      answer = answer + "beta";
      break;
      case 3:
      answer = answer + "gamma";
      break;
      case 4:
      answer = answer + "delta";
      break;
    }
    return answer;
  }